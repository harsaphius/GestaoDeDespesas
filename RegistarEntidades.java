    private void btn_AddCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddCategoriaActionPerformed
        
        Create createentidade = new Create();
        NoRepeat norepeat = new NoRepeat();
        
        if(txt_InserirEntidade.getText().length()>0){
            createentidade.entidade = txt_InserirEntidade.getText();
            createentidade.nif = Integer.parseInt(txt_NifEntidade.getText());
            createentidade.contacto = txt_Contacto.getText();
            createentidade.estado = true;
            createentidade.codentidade = null;
            
        }else
            JOptionPane.showMessageDialog(null, "Insira uma entidade");
        
        
        norepeat.entidades = txt_InserirEntidade.getText();
        if(norepeat.verifyRepetedEntidades()){
            JOptionPane.showMessageDialog(null,"A Entidade j√° existe!");
            txt_InserirEntidade.setText(null);
        }else{
            
            if(createentidade.CreateNewEntidade()==true)
            {
                txt_InserirEntidade.setText(null);
                txt_NifEntidade.setText(null);
                txt_Contacto.setText(null);
            //this.dispose();
            }
        }
    }//GEN-LAST:event_btn_AddCategoriaActionPerformed

