    private void btn_AddCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddCategoriaActionPerformed
      
        Create createtpagamento = new Create();
        NoRepeat norepeat = new NoRepeat();
        
        if(txt_InserirTipoPagamento.getText().length()>0){
            createtpagamento.tipopagamento = txt_InserirTipoPagamento.getText();
            createtpagamento.estado = true;

            
        }else
            JOptionPane.showMessageDialog(null, "Insira um Tipo de Pagamento");
        
        
        norepeat.tipopagamento = txt_InserirTipoPagamento.getText();
        if(norepeat.verifyRepetedTipoPagamento()){
            JOptionPane.showMessageDialog(null,"O Tipo de Pagamanto j√° existe!");
            txt_InserirTipoPagamento.setText(null);
        }else{
            
            if(createtpagamento.CreateNewTipoPagamento()==true)
            {
                txt_InserirTipoPagamento.setText(null);

            //this.dispose();
            }
        }

    }//GEN-LAST:event_btn_AddCategoriaActionPerformed

